    static function stringToAlias($string)
    {
        $alias = trim(strtolower($string));
        $alias = str_replace("'", "", $alias);
        $alias = str_replace(",", "", $alias);
        $alias = strtr($alias, static::$charSet);
        $alias = str_replace("`", "", $alias);
        $alias = str_replace(" ", "-", $alias);
        $alias = str_replace('&', '-and-', $alias);
        $alias = str_replace('--', '-', $alias);
        $alias = preg_replace('![^\w\d\s-]*!', '', $alias);
        $alias = urlencode($alias);

        return $alias;
    }    
    
